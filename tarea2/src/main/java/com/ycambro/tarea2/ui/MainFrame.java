    private void agregarBot1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBot1ActionPerformed
        List<Acompannamiento> acomps = AcompannamientoDAO.obtenerTodos();
        List<Proteina> proteinas = ProteinaDAO.obtenerTodos();
        List<Bebida> bebidas = BebidaDAO.obtenerTodos();
        List<Postre> postres = PostreDAO.obtenerTodos();
        Acompannamiento acid = null;
        Proteina prid = null;
        Bebida beid = null;
        Postre poid = null;
        for (Acompannamiento acom : acomps) {
            if (obAc1.getSelectedItem().toString().equals(acom.getNombre())){
                acid = acom;
            }
        }
        for (Proteina acom : proteinas) {
            if (obAc1.getSelectedItem().toString().equals(acom.getNombre())){
                prid = acom;
            }
        }
        if (obAc9.getSelectedIndex() == 0) {
            app.annadirPlato(new Plato(99, nombreA1.getText(), obAc9.getSelectedIndex()+1, acid, prid, (acid.getCalorias() + prid.getCalorias()), (acid.getPrecio() + prid.getPrecio()), obAc7.getSelectedItem().toString(), obAc6.getSelectedItem().toString(), obAc8.getSelectedItem().toString()));
        } else if (obAc9.getSelectedIndex() == 1) {
            for (Bebida acom : bebidas) {
                if (obAc1.getSelectedItem().toString().equals(acom.getNombre())){
                    beid = acom;
                }
            }
            app.annadirPlato(new PlatoBebida(99, nombreA1.getText(), obAc9.getSelectedIndex()+1, acid, prid, beid, (acid.getCalorias() + prid.getCalorias()), (acid.getPrecio() + prid.getPrecio()), obAc7.getSelectedItem().toString(), obAc6.getSelectedItem().toString(), obAc8.getSelectedItem().toString()));
        } else if (obAc9.getSelectedIndex() == 2) {
            for (Bebida acom1 : bebidas) {
                if (obAc1.getSelectedItem().toString().equals(acom1.getNombre())){
                    beid = acom1;
                }
            }
            for (Postre acom : postres) {
                if (obAc1.getSelectedItem().toString().equals(acom.getNombre())){
                    poid = acom;
                }
            }
            app.annadirPlato(new PlatoCompleto(99, nombreA1.getText(), obAc9.getSelectedIndex()+1, acid, prid, beid, poid, (acid.getCalorias() + prid.getCalorias()), (acid.getPrecio() + prid.getPrecio()), obAc7.getSelectedItem().toString(), obAc6.getSelectedItem().toString(), obAc8.getSelectedItem().toString()));
        }
    }//GEN-LAST:event_agregarBot1ActionPerformed

